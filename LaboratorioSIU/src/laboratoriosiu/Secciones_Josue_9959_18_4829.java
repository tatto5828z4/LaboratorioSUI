    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed
        // TODO add your handling code here:

        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/SIU", "root", "Langas798");
            PreparedStatement pst = cn.prepareStatement("select * from Secciones where Codigo_Secciones = ?");
            pst.setString(1, txt_Buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next())
            {
                txt_Codigo_Secciones.setText(rs.getString("codigo_secciones"));
                txt_Nombre_Secciones.setText(rs.getString("nombre_secciones"));
                txt_Estatus_Secciones.setText(rs.getString("estatus_secciones"));
            } else
            {
                JOptionPane.showMessageDialog(null, "Seccion no registrada.");
            }
        }catch (Exception e)
        {

        }
    }//GEN-LAST:event_jButton_BuscarActionPerformed


